	template<typename T>
	concept SupportedQueryType =
		std::same_as<T, int> || 
		std::same_as<T, int64_t> || 
		std::same_as<T, bool> || 
		std::same_as<T, float> || 
		std::same_as<T, double> ||
		std::same_as<T, std::string>;


