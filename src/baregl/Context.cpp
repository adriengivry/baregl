	template<>
	void GetValue<bool>(
		baregl::types::EGetParameter p_param,
		std::span<bool> p_out
	)
	{
		std::vector<GLboolean> values(p_out.size());
		glGetBooleanv(
			baregl::utils::EnumToValue<GLenum>(p_param),
			values.data()
		);

		for (size_t i = 0; i < p_out.size(); ++i)
		{
			p_out[i] = values[i] != GL_FALSE;
		}
	}

